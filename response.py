
import pandas as pd
import numpy as np

def answer(df):
    
    ''' 
    The df dtypes are:
    {'Quantity': 'number[int32]'}
            
    
    Returns : What are the three least commonly ordered quantities?
    Return Type : list[number]
    '''
             
            
    # Get the counts of each quantity
    counts = df['Quantity'].value_counts()
    
    # Get the three least common quantities
    least_common = counts.nsmallest(3).index.tolist()
    
    return least_common














































































































































































































































df = pd.read_parquet("data/lite/065_RFM.parquet")
print(answer(df))
            